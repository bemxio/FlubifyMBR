    printf("FlubifyMBR v%s\n", VERSION);
    printf("A silly program to overwrite the MBR with a bootloader that will show what you want!\n\n");

    printf("Enter the text that will be shown upon booting (max 487 characters, the rest will be stripped off): \n");
